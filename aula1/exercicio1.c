#include <stdio.h>
#include <stdlib.h>

//------------------------------------------------------------------------------------------------
//-------------------------------------VARIÁVEIS--------------------------------------------------
//------------------------------------------------------------------------------------------------

char cliente1[30] = "Gustavo Gregory";
char cliente2[30] = "Cesar Gregory";

int numeroConta1 = 12345;
int numeroConta2 = 54321;

float saldo1 = 250.00;
float saldo2 = 300.00;

//------------------------------------------------------------------------------------------------
//---------------------------------------FUNÇÕES--------------------------------------------------
//------------------------------------------------------------------------------------------------

void imprimeConta(char *conta, int numeroConta, float saldo, int num)
{
    printf("\nConta %d:\n", num);
    printf("\n-Cliente: %s\n", conta);
    printf("\n-Numero: %d\n", numeroConta);
    printf("\n-Saldo: %.2f\n", saldo);
}

void debitarConta(float *saldo, int valor)
{
    *saldo = *saldo - valor;
}

void creditarConta(float *saldo, int valor)
{
    *saldo = *saldo + valor;
}

//------------------------------------------------------------------------------------------------
//---------------------------------FUNÇÃO MAIN----------------------------------------------------
//------------------------------------------------------------------------------------------------

int main()
{
    printf("\n---------------------------------\n");
    imprimeConta(cliente1, numeroConta1, saldo1, 1);
    printf("\n---------------------------------\n");
    imprimeConta(cliente2, numeroConta2, saldo2, 2);

    printf("\n---------------------------------\n");
    printf("\nDebitar 500 da conta 1           \n");
    printf("\nCreditar 50 na conta 2           \n");
    printf("\n---------------------------------\n");

    debitarConta(&saldo1, 500);
    creditarConta(&saldo2, 50);

    printf("\n---------------------------------\n");
    imprimeConta(cliente1, numeroConta1, saldo1, 1);
    printf("\n---------------------------------\n");
    imprimeConta(cliente2, numeroConta2, saldo2, 2);

    return 0;
}